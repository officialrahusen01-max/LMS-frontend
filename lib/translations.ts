type Locale = keyof typeof translations;

function get(obj: unknown, path: string): string | string[] | undefined {
  const keys = path.split(".");
  let current: unknown = obj;
  for (const k of keys) {
    if (current == null || typeof current !== "object") return undefined;
    current = (current as Record<string, unknown>)[k];
  }
  return current as string | string[] | undefined;
}

export function getTranslation(locale: Locale, key: string): string | string[] | undefined {
  return get(translations[locale], key) ?? get(translations.en, key);
}

export function useTranslation(locale: Locale) {
  const t = (key: string): string => {
    const val = getTranslation(locale, key);
    if (Array.isArray(val)) return val[0] ?? key;
    return (val as string) ?? key;
  };
  const tArray = (key: string): string[] => {
    const val = getTranslation(locale, key);
    return Array.isArray(val) ? val : val ? [val] : [];
  };
  return { t, tArray, locale };
}

import { useLanguage } from "./language-store";

/** Hook for components: uses current locale from context and returns t / tArray. */
export function useTranslationHook() {
  const { locale } = useLanguage();
  return useTranslation(locale);
}
